class GroupServiceAsyncImpl constructor(private val clientOptions: ClientOptions,) : GroupServiceAsync {

    private val errorHandler: Handler<IncreaseError> = errorHandler(clientOptions.jsonMapper)

    private val retrieveDetailsHandler: Handler<Group> =
    jsonHandler<Group>(clientOptions.jsonMapper)
    .withErrorHandler(errorHandler)

    /** Returns details for the currently authenticated Group. */
    override fun retrieveDetails(
        params: GroupRetrieveDetailsParams,
        requestOptions: RequestOptions
    ): CompletableFuture<Group> {
        val request =
            HttpRequest.builder()
                .method(HttpMethod.GET)
                .addPathSegments("groups", "current")
                .putAllQueryParams(params.getQueryParams())
                .putAllHeaders(clientOptions.headers)
                .putAllHeaders(params.getHeaders())
                .build()
        return clientOptions.httpClient.executeAsync(request, requestOptions).thenApply { response
            ->
            response
                .let { retrieveDetailsHandler.handle(it) }
                .apply {
                    if (requestOptions.responseValidation ?: clientOptions.responseValidation) {
                        validate()
                    }
                }
        }
    }
}
